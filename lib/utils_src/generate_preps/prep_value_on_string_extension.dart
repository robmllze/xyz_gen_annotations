extension PrepOnStringExtension on String {
  /// Extracts the prep value.
  String get prepValue {
    final match = prepRegExp.firstMatch(this)?[3];
    if (match != null) {
      return match;
    }
    return '';
  }
}

@internal
final prepRegExp = RegExp(r'<<<((.+)\|\|)? *([\w-&]+)>>>');
