const DATA_REF_MODEL_FIELDS = {
  Field(
    fieldName: 'ref',
    fieldType: DataRefModel,
  ),
};

@GenerateModel(
  shouldInherit: true,
  fields: {
    ...DATA_REF_MODEL_FIELDS,
  },
